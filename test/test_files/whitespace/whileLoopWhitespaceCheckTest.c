int main(int argc, char** argv) {

    // while loops should look like while (...
    while 
            (argc) {
    }

    // Whitespace between while and (
    while  (argc) {
    }


    // Condition content on the wrong line
    while (
            argc
            ) {
    }

    // Too much condition whitespace
    while ( argc ) {
    }

    // Too must whitespace between ) and {
    while (argc)  {
    }

    return 0;
}